CREATE TABLE if not exists "horse_results" (
	"horse_id"	TEXT NOT NULL,
	"race_id"	TEXT,
	"frame_no"	INTEGER,
	"horse_no"	INTEGER,
	"win_odds"	REAL,
	"pupularity"	INTEGER,
	"arriving_order"	NUMERIC,
	"jockey_id"	TEXT,
	"impost"	REAL,
	"goal_time"	TEXT,
	"time_diff"	REAL,
	"corner_pass"	TEXT,
	"pase"	TEXT,
	"last_three_furlong"	REAL,
	"horse_weight"	TEXT,
	"prise"	REAL,
	FOREIGN KEY("horse_id") REFERENCES "horse"("id"),
	PRIMARY KEY("horse_id","race_id")
)