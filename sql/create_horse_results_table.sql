CREATE TABLE "horse_results" (
	"horse_id"	TEXT NOT NULL,
	"date"	INTEGER NOT NULL,
	"place"	TEXT,
	"weather"	TEXT,
	"race_no"	INTEGER,
	"race_name"	TEXT,
	"horse_num"	INTEGER,
	"frame_no"	INTEGER,
	"horse_no"	INTEGER,
	"win_odds"	REAL,
	"popular_order"	INTEGER,
	"arriving_order"	TEXT,
	"jockey_id"	TEXT,
	"impost"	REAL,
	"race_type"	TEXT,
	"distance"	INTEGER,
	"ground"	TEXT,
	"goal_time"	TEXT,
	"time_diff"	REAL,
	"corner_pass"	TEXT,
	"pase"	TEXT,
	"last_three_furlong"	TEXT,
	"horse_weight"	TEXT,
	"prise"	REAL,
	PRIMARY KEY("horse_id","date"),
	FOREIGN KEY("horse_id") REFERENCES "horse"("id"),
	FOREIGN KEY("jockey_id") REFERENCES "jockey"("id")
)